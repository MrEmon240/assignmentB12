

document.querySelector(".coll").addEventListener("click", function () {
 alert("National Emergency Number 999");
   
  const collCount = document.querySelector(".collCount");
  const count = parseInt(collCount.innerText);
    if (count >! 20) {
        collCount.innerText = count - 20;
    }
    else{
      alert("You don't have enough coins.");
    }
});

